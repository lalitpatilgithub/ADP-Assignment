package com.adp.VehicleIdentifier;

/**
 * 
 * @author Lalit Patil
 * 
 */

public class Vehicle {
	

	
	

	private String vehicleId;
	private String frameMaterial;
	private Integer numberOfWheels;
	private String materialOfWheel;
	private String powerTrain;
	
	
	public String getVehicleId() {
		return vehicleId;
	}
	public void setVehicleId(String vehicleId) {
		this.vehicleId = vehicleId;
	}
	public String getFrameMaterial() {
		return frameMaterial;
	}
	public void setFrameMaterial(String frameMaterial) {
		this.frameMaterial = frameMaterial;
	}
	public Integer getNumberOfWheels() {
		return numberOfWheels;
	}
	public void setNumberOfWheels(Integer numberOfWheels) {
		this.numberOfWheels = numberOfWheels;
	}
	public String getMaterialOfWheel() {
		return materialOfWheel;
	}
	public void setMaterialOfWheel(String materialOfWheel) {
		this.materialOfWheel = materialOfWheel;
	}
	public String getPowerTrain() {
		return powerTrain;
	}
	public void setPowerTrain(String powerTrain) {
		this.powerTrain = powerTrain;
	}


	
	


}
